    valor VARCHAR(255),
    PRIMARY KEY (id_consulta, id_parte_grafico),
    CONSTRAINT fk_rg_consulta FOREIGN KEY (id_consulta) REFERENCES consultas(id)
        ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT fk_rg_parte_grafico FOREIGN KEY (id_parte_grafico) REFERENCES parte_grafico(id)
        ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
CREATE TABLE grafica_formula(
    id_parte_grafico INT PRIMARY KEY UNIQUE,
    origen VARCHAR(50),
    formula VARCHAR(50),
    FOREIGN KEY (id_parte_grafico) REFERENCES parte_grafico(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
