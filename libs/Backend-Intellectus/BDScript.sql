USE IntellectusDB;
-- Independientes
CREATE TABLE areas (
    id INT AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(50) NOT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
CREATE TABLE listados (
    id INT AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(255) NOT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
CREATE TABLE pacientes (
    id INT AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(255) NOT NULL,
    cedula VARCHAR(50) NOT NULL UNIQUE,
    fecha_nacimiento DATE NOT NULL,
    lateralidad VARCHAR(50) NOT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
CREATE TABLE medicos(
    id INT AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(100),
    especialidad VARCHAR(200)
)ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
CREATE TABLE tipo_de_transtorno (
    id INT AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(255) NOT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
-- Dependencia 1
CREATE TABLE categorias (
    id INT AUTO_INCREMENT PRIMARY KEY,
    id_area INT NOT NULL,
    nombre VARCHAR(255) NOT NULL,
    tipo_resultado VARCHAR(50),
    visibilidad Boolean,
    CONSTRAINT fk_categoria_area FOREIGN KEY (id_area) REFERENCES areas(id)
        ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
CREATE TABLE transtorno_general (
    id INT AUTO_INCREMENT PRIMARY KEY,
    id_tipo_de_transtorno INT NOT NULL,
    nombre VARCHAR(255) NOT NULL,
    CONSTRAINT fk_transtorno_general_tipo FOREIGN KEY (id_tipo_de_transtorno) REFERENCES tipo_de_transtorno(id)
        ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
CREATE TABLE opciones_listados (
    id INT AUTO_INCREMENT PRIMARY KEY,
    id_listado INT NOT NULL,
    texto VARCHAR(500) NOT NULL,
    tipo_valor VARCHAR(50),
    valor VARCHAR(255),
    CONSTRAINT fk_opcion_listado FOREIGN KEY (id_listado) REFERENCES listados(id)
        ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
CREATE TABLE consultas (
    id INT AUTO_INCREMENT PRIMARY KEY,
    id_paciente INT NOT NULL,
    fecha_evaluacion DATE NOT NULL,
    edad_paciente INT NOT NULL,
    CONSTRAINT fk_consulta_paciente FOREIGN KEY (id_paciente) REFERENCES pacientes(id)
        ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
--  Dependencia 2
CREATE TABLE preguntas (
    id INT AUTO_INCREMENT PRIMARY KEY,
    id_categoria INT NOT NULL,
    texto_pregunta VARCHAR(500) NOT NULL,
    tipo_respuesta VARCHAR(50) NOT NULL,
    visibilidad Boolean,
    CONSTRAINT fk_pregunta_categoria FOREIGN KEY (id_categoria) REFERENCES categorias(id)
        ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
CREATE TABLE pregunta_listado (
    id_pregunta INT PRIMARY KEY UNIQUE,
    id_listado INT NOT NULL,
    FOREIGN KEY (id_pregunta) REFERENCES preguntas(id) ON DELETE CASCADE,
    FOREIGN KEY (id_listado) REFERENCES listados(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
create Table consulta_medico(
    id_consulta INT NOT NULL,
    id_medico INT NOT NULL,
    CONSTRAINT fk_consulta_medico_consulta FOREIGN KEY (id_consulta) REFERENCES consultas(id),
    CONSTRAINT fk_consulta_medico_medico FOREIGN KEY (id_medico) REFERENCES medicos(id)
)ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
CREATE TABLE transtorno_especifico (
    id INT AUTO_INCREMENT PRIMARY KEY,
    id_transtorno_general INT NOT NULL,
    DSM_V VARCHAR(255),
    CIE_10 VARCHAR(255),
    nombre VARCHAR(255) NOT NULL,
    CONSTRAINT fk_transtorno_especifico_general FOREIGN KEY (id_transtorno_general) REFERENCES transtorno_general(id)
        ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
CREATE TABLE respuesta_acumulado (
    id_consulta INT NOT NULL,
    id_categoria INT NOT NULL,
    valor VARCHAR(3000),
    PRIMARY KEY (id_consulta, id_categoria),
    CONSTRAINT fk_ra_consultaAcumulada FOREIGN KEY (id_consulta) REFERENCES consultas(id)
        ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT fk_ra_categoriaAcumulada FOREIGN KEY (id_categoria) REFERENCES categorias(id)
        ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
CREATE TABLE archivos_consulta (
    id INT AUTO_INCREMENT PRIMARY KEY,
    id_consulta INT NOT NULL,
    tipo_archivo VARCHAR(100) NOT NULL,
    ruta_archivo VARCHAR(500) NOT NULL,
    CONSTRAINT fk_archivo_consulta FOREIGN KEY (id_consulta) REFERENCES consultas(id)
        ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
CREATE TABLE grafica (
    id INT AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(255) NOT NULL,
    tipo VARCHAR(100),
    id_categoria INT NOT NULL UNIQUE,
    CONSTRAINT fk_grafica_categoria FOREIGN KEY (id_categoria) REFERENCES categorias(id)
        ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
-- Dependencia 3
CREATE TABLE referencias (
    id INT AUTO_INCREMENT PRIMARY KEY,
    valor_tomar_pregunta INT NULL,
    origen VARCHAR(50),
    tipo VARCHAR(50),
    nombre VARCHAR(255),
    CONSTRAINT fk_valor_tomar_pregunta FOREIGN KEY (valor_tomar_pregunta) REFERENCES preguntas(id)
        ON DELETE SET NULL ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
CREATE TABLE pregunta_formula(
    id_pregunta INT PRIMARY KEY UNIQUE,
    origen VARCHAR(50),
    formula VARCHAR(50),
    FOREIGN KEY (id_pregunta) REFERENCES preguntas(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
CREATE TABLE respuesta_abierta (
    id_consulta INT NOT NULL,
    id_pregunta INT NOT NULL,
    valor VARCHAR(10000),
    PRIMARY KEY (id_consulta, id_pregunta),
    CONSTRAINT fk_ra_consultaAbierta FOREIGN KEY (id_consulta) REFERENCES consultas(id)
        ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT fk_ra_preguntaAbierta FOREIGN KEY (id_pregunta) REFERENCES preguntas(id)
        ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
CREATE TABLE respuesta_cerrada (
    id_consulta INT NOT NULL,
    id_pregunta INT NOT NULL,
    id_opcion_listado INT NOT NULL,
    PRIMARY KEY (id_consulta, id_pregunta),
    CONSTRAINT fk_ra_consultaCerrada FOREIGN KEY (id_consulta) REFERENCES consultas(id)
        ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT fk_ra_preguntaCerrada FOREIGN KEY (id_pregunta) REFERENCES preguntas(id)
        ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT fk_ra_opcion_listado FOREIGN KEY (id_opcion_listado) REFERENCES opciones_listados(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
CREATE TABLE seccion_grafico (
    id INT AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(255) NOT NULL,
    color VARCHAR(50),
    id_grafica INT NOT NULL,
    CONSTRAINT fk_seccion_grafica FOREIGN KEY (id_grafica) REFERENCES grafica(id)
        ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
CREATE TABLE transtorno_especifico_consulta (
    id_consulta INT NOT NULL,
    id_transtorno_especifico INT NOT NULL,
    PRIMARY KEY (id_consulta, id_transtorno_especifico),
    CONSTRAINT fk_te_consulta FOREIGN KEY (id_consulta) REFERENCES consultas(id)
        ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT fk_te_especifico FOREIGN KEY (id_transtorno_especifico) REFERENCES transtorno_especifico(id)
        ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
-- Dependencia 4
CREATE TABLE pregunta_referencia (
    id_pregunta INT PRIMARY KEY UNIQUE,
    id_referencia INT NOT NULL,
    FOREIGN KEY (id_pregunta) REFERENCES preguntas(id) ON DELETE CASCADE,
    FOREIGN KEY (id_referencia) REFERENCES referencias(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
CREATE TABLE valores (
    id INT AUTO_INCREMENT PRIMARY KEY,
    id_referencia INT NOT NULL,
    condicional VARCHAR(255),
    valor_condicional VARCHAR(255),
    CONSTRAINT fk_valor_referencia FOREIGN KEY (id_referencia) REFERENCES referencias(id)
        ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
CREATE TABLE parte_grafico (
    id INT AUTO_INCREMENT PRIMARY KEY,
    texto VARCHAR(255) NOT NULL,
    valor VARCHAR(255),
    id_seccion_grafico INT NOT NULL,
    CONSTRAINT fk_parte_seccion FOREIGN KEY (id_seccion_grafico) REFERENCES seccion_grafico(id)
        ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
